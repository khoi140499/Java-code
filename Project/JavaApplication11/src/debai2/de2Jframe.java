    private void btnChamcongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChamcongActionPerformed

        NhanVien nhanVien = new NhanVien();
        PhongBan phong = new PhongBan();
        for(NhanVien nv : nhanViens){
            if(txtMa.getText().equals(Integer.toString(nv.getMaVN()))){
                nhanVien = nv;
                break;
            }
        }
        for(PhongBan pb : phongBans){
             if(txtPhongBan.getText().equals(Integer.toString(pb.getMaPb()))){
                phong = pb;
                break;
            }
        }
        nhanVien.setPhongBan(phong);
        listChamCong.add(new Pair<>(nhanVien, Integer.parseInt(txtSoNgay.getText())));
         
        DefaultTableModel dtm = (DefaultTableModel) bangChamCong.getModel();
        Vector<Object> vec = new Vector<Object>();
        vec.add(nhanVien.getMaVN());
        vec.add(nhanVien.getHoTen());
        vec.add(txtSoNgay.getText());
        vec.add(nhanVien.getBacLuong());
        vec.add(phong.getTenPhong());
        dtm.addRow(vec);
        bangChamCong.setModel(dtm);
        
    }//GEN-LAST:event_btnChamcongActionPerformed

    private void btnSapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSapXepActionPerformed
        sortByName();
        DefaultTableModel dtm = (DefaultTableModel) bangChamCong.getModel();
        dtm.setRowCount(0);
        for(NhanVien nv : nhanViens ){
            Vector<Object> vec = new Vector<Object>();
            vec.add(nv.getMaVN());
            vec.add(nv.getHoTen());
            Pair pair = new Pair();
            for(Pair p : listChamCong){
                if(p.getKey() == nv){
                    pair = p;
                }
            }
            vec.add(pair.getValue());
            vec.add(nv.getBacLuong());
            vec.add(nv.getPhongBan().getTenPhong());
            dtm.addRow(vec);
        }
        bangChamCong.setModel(dtm);
        
    }//GEN-LAST:event_btnSapXepActionPerformed

