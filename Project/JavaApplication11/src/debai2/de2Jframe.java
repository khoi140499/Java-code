    private void importNhanVien() {
        soNV = Integer.parseInt(in.nextLine());
        for(int i =0; i<soNV; i++){
            int ma = Integer.parseInt(in.nextLine());
            String hoTen = in.nextLine();
            String diaChi = in.nextLine();
            String sdt = in.nextLine();
            int bacLuong  = Integer.parseInt(in.nextLine());          
            
            NhanVien nv = new NhanVien(ma,hoTen,diaChi,sdt,bacLuong);
            nhanViens.add(nv);
        }
    }

    private void loadNhanVien() {
        DefaultTableModel dtm = (DefaultTableModel) bangNhanVien.getModel();
        dtm.setRowCount(0);
        for(NhanVien nv : nhanViens){
            Vector<Object> vec = new Vector<Object>();
            vec.add(nv.getMaVN());
            vec.add(nv.getHoTen());
            vec.add(nv.getDiaChi());
            vec.add(nv.getSdt());
            vec.add(nv.getBacLuong());
            dtm.addRow(vec);
        }
        bangNhanVien.setModel(dtm);
    }

    private void importPhongBan() {
        soPb = Integer.parseInt(in.nextLine());
        for(int i = 0;i <soPb; i++){
            int maPh = Integer.parseInt(in.nextLine());
            String tenPB = in.nextLine();
            String moTa = in.nextLine();
            int heSoVC = Integer.parseInt(in.nextLine());
            
            PhongBan pb = new PhongBan(maPh, tenPB, moTa, heSoVC);
            phongBans.add(pb);
        }
    }

    private void loadPhongBan() {
        DefaultTableModel dtm = (DefaultTableModel) bangPhongBan.getModel();
        dtm.setRowCount(0);
        for(PhongBan pb:  phongBans){
            Vector<Object> vec = new Vector<Object>();
            vec.add(pb.getMaPb());
            vec.add(pb.getTenPhong());
            vec.add(pb.getMoTa());
            vec.add(pb.getHesoCV());
            dtm.addRow(vec);
        }
        bangPhongBan.setModel(dtm);
        
    }

    private void sortByName() {
        Comparator<NhanVien> comp = new Comparator<NhanVien>() {
            @Override
            public int compare(NhanVien o1, NhanVien o2) {
                return o1.getHoTen().compareTo(o2.getHoTen());
            }
        };
        Collections.sort(nhanViens, comp);
    }
