    private void kiemTra(){
        String user="";
        String pass="";
        String kt="";
        try {
            File f=new File("Account.txt");
            Scanner sc=new Scanner(f);
            while (sc.hasNext()) {
                user=sc.nextLine();
                pass=sc.nextLine();
                kt=sc.nextLine();
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        if(kt.equals("true")==true){
            txtUser.setText(user);
            txtPass.setText(pass);
            ckbLuu.setSelected(true);
        }
        else if(kt.equals("false")==true){
            txtUser.setText("");
            txtPass.setText("");
            ckbLuu.setSelected(false);
        }
    }
