    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtUser.getText().isEmpty()){
            try {
                throw new StringIndexOutOfBoundsException("Tên đăng nhập không được để trống");
            } catch (StringIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        else if(txtPass.getText().isEmpty()){
            try {
                throw new StringIndexOutOfBoundsException("Mật khẩu không được để trống");
            } catch (StringIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        else{
            try {
                String url="jdbc:mysql://localhost/banhangonline";
                String user="root";
                String pass="";
                conn=(Connection) DriverManager.getConnection(url, user, pass);
                String s="select user, pass from khachhang where user=? and pass=?";
                PreparedStatement ps=conn.prepareStatement(s);
                ps.setString(1, txtUser.getText());
                ps.setString(2, txtPass.getText());
                ResultSet rs=ps.executeQuery();
                if(rs.next()){
                    if(ckbLuu.isSelected()){
                        try {
                            File f=new File("Account.txt");
                            PrintWriter pr=new PrintWriter(f);
                            pr.println(txtUser.getText());
                            pr.println(txtPass.getText());
                            pr.println("true");
                            pr.close();
                        } catch (Exception e) {
                            System.out.println(e);
                        }
                    }
                    else{
                        try {
                            File f=new File("Account.txt");
                            PrintWriter pr=new PrintWriter(f);
                            pr.println("");
                            pr.println("");
                            pr.println("false");
                            pr.close();
                        } catch (Exception e) {
                            System.out.println(e);
                        }
                    }
                    try {
                        File f=new File("Ten.txt");
                        PrintWriter pr=new PrintWriter(f);
                        pr.println(txtUser.getText());
                        pr.close();
                    } catch (Exception e) {
                        System.out.println(e);
                    }
                    setVisible(false);
                    MuaHang a=new MuaHang();
                    a.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Tên đăng nhập hoặc mật khẩu không đúng");
                }
            } catch (Exception e) {
                System.out.println(e);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

