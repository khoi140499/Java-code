    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if(txtTenSP.getText().isEmpty()){
                hien("Tên sản phẩm không được để trống");
            }
            else if(Double.parseDouble(txtGiaNhap.getText())<0){
                JOptionPane.showMessageDialog(null, "Giá nhập phải lớn hơn 0");
            }
            else if(Double.parseDouble(txtGiaBan.getText())<0){
                JOptionPane.showMessageDialog(null, "Giá bán phải lớn hơn 0");
            }
            else if(txtDonVi.getText().isEmpty()){
                hien("Đơn vị tính không đưuọc để trống");
            }
            else if(txtHangSX.getText().isEmpty()){
                hien("Hãng sản xuất không được để trống");
            }
            else if(Integer.parseInt(txtSoLuong.getText())<0){
                JOptionPane.showMessageDialog(null, "Số lượng nhập phải lớn hơn 0");
            }
            else{
                try {
                    int m=1+sp.size();
                    String url="jdbc:mysql://localhost/banhangonline";
                    String user="root";
                    String pass="";
                    conn=(Connection) DriverManager.getConnection(url, user, pass);
                    String ssa="insert into sanpham values(?,?,?,?,?,?,?,?,?)";
                    PreparedStatement ps=conn.prepareStatement(ssa);
                    ps.setInt(1, m);
                    ps.setString(2, txtTenSP.getText());
                    ps.setString(3, comboLoaiSP.getSelectedItem().toString());
                    ps.setString(4, txtHangSX.getText());
                    ps.setDouble(5, Double.parseDouble(txtGiaNhap.getText()));
                    ps.setDouble(6, Double.parseDouble(txtGiaBan.getText()));
                    ps.setInt(7, Integer.parseInt(txtSoLuong.getText()));
                    ps.setString(8, txtDonVi.getText());
                    ps.setString(9, this.t);
                    ps.executeUpdate();
                    SanPham a=new SanPham(m, txtTenSP.getText(), comboLoaiSP.getSelectedItem().toString(),
                            txtHangSX.getText(), Double.parseDouble(txtGiaNhap.getText()), Double.parseDouble(txtGiaBan.getText()),
                            Integer.parseInt(txtSoLuong.getText()), txtDonVi.getText(), this.t);
                    sp.add(a);
                    dl2.addElement(txtTenSP.getText());
                } catch (Exception e) {
                    System.out.println(e);
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Lỗi");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

