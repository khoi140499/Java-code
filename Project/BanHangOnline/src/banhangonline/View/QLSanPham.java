    private void listMHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listMHMouseClicked
        String r=listMH.getSelectedValue();
        for(SanPham as:sp){
            if(as.getTenSP().equals(r)==true){
                txtTenSP.setText(as.getTenSP());
                txtGiaNhap.setText(as.getGiaNhap()+"");
                txtGiaBan.setText(as.getGiaBan()+"");
                txtHangSX.setText(as.getHangSX());
                txtDonVi.setText(as.getDonviTinh());
                txtSoLuong.setText(as.getSoLuong()+"");
                ImageIcon img=new ImageIcon(as.getHinhAnh());
                lb.setIcon(img);
                String s=as.getMaLoai();
                for(int i=0;i<comboLoaiSP.getItemCount();i++){
                    if(comboLoaiSP.getItemAt(i).equals(s)==true){
                        comboLoaiSP.setSelectedIndex(i);
                    }
                }
            }
        }
    }//GEN-LAST:event_listMHMouseClicked
