
package ghepchuoi;

import java.util.*;


public class Ghepchuoi {


    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        StringBuilder sb=new StringBuilder();
        String s=sc.nextLine();
        int n=sc.nextInt();
        for(int i=1;i<=n;i++){
            sb.append(s);
        }
        System.out.println(sb);
    }
    
}
