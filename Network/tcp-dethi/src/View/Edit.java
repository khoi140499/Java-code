    public void inRe(Message msg) {
        switch(msg.getType()){
            case 1:{
                int a=JOptionPane.YES_NO_OPTION;
                int b=JOptionPane.showConfirmDialog(null, msg.getObj(), "Ket qua", a);
                if(b==0){
                    setVisible(false);
                    new Home().setVisible(true);
                }
                break;
            }
        }
    }
