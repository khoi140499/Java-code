    @Override
    public void inRe(Message msg) {
        switch(msg.getType()){
            case 0:{
                ArrayList<SinhVien> sv=msg.getSv();
                for(SinhVien as:sv){
                    Vector v3=new Vector();
                    v3.add(as.getMa());
                    v3.add(as.getTen());
                    v3.add(as.getNgaySinh());
                    v3.add(as.getKhoa());
                    v3.add(as.getQuequan());
                    v2.add(v3);
                }
                tb.updateUI();
                break;
            }
        }
    }
