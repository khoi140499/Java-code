    private void connect() {
        try {
            if (socket == null) {
                socket = new Socket("127.0.0.1", 4444);
                cl = new ClientListen(socket);
                cl.rc = this;
                cl.start();
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }

