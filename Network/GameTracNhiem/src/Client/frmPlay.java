    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for(Question as:questions){
            if(as.getQuestion().equalsIgnoreCase(txtCauHoi.getText())==true){
                if(ckA.isSelected()==true){
                    dapan=ckA.getText();
                }
                if(ckB.isSelected()==true){
                    dapan=ckB.getText();
                }
                if(ckC.isSelected()==true){
                    dapan=ckC.getText();
                }
                if(ckD.isSelected()==true){
                    dapan=ckD.getText();
                }
            }
        }
        
        for(Question as:questions){
            if(as.getQuestion().equalsIgnoreCase(txtCauHoi.getText())==true){
                if(as.getCorrectAnswer().equalsIgnoreCase(dapan)==true){
                    dem+=1;
                    diem.setText(dem+"");
                }
            }
        }
        
        for(int i=0;i<questions.size();i++){
            s=questions.get(stt+1).getQuestion();
        }
        
        for(Question as:questions){
            if(as.getQuestion().equalsIgnoreCase(s)==true){
                txtCauHoi.setText(s);
                ckA.setText(as.getAnswerA());
                ckB.setText(as.getAnswerB());
                ckC.setText(as.getAnswerC());
                ckD.setText(as.getAnswerD());
                jLabel2.setText("Câu số "+stt+2);
                stt+=1;
            }
        }
        
        if(stt==7){
            jButton1.setEnabled(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

