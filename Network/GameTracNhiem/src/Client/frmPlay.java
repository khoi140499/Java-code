    @Override
    public void ReceiveMessage(Message msg) throws IOException {
        switch (msg.getType()) {
            // Nhận yêu cầu chơi 
            case 41: {
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(this, "Sẵn sàng chơi?", "Title on Box", dialogButton);
                if (dialogResult == 0) {
                    listenServer.SendMessage(44, null);// Gửi chấp nhận
                } else {
                    listenServer.SendMessage(45, null);// Gửi từ chối 
                }
                break;
            }
            // Người chơi đồng ý và nhận dự liệu
            case 46: {
                questions = msg.getQuestions();// Nhận câu hỏi
                for(int i=0;i<questions.size();i++){
                    s=questions.get(0).getQuestion();
                    break;
                }
                for(Question as:questions){
                    if(as.getQuestion().equalsIgnoreCase(s)==true){
                        txtCauHoi.setText(as.getQuestion());
                        ckA.setText(as.getAnswerA());
                        ckB.setText(as.getAnswerB());
                        ckC.setText(as.getAnswerC());
                        ckD.setText(as.getAnswerD());
                        jLabel2.setText("Câu số "+stt+1);
                        socauhoi=1;
                        stt=0;
                    }
                }
                break;
            }
            // Người chơi không đồng ý
            case 47: {
                JOptionPane.showMessageDialog(null, "Người chơi không đồng ý!", "Message", 1);
                break;
            }
            case 60: {
                game2 = (Game) msg.getObject();
                String t = "";
                this.setVisible(false);
                frmScore resultForm = new frmScore(listenServer, game2, t);
                resultForm.setVisible(true);
                break;
            }
            // Thông báo 
            case 61: {
                game2 = (Game) msg.getObject();
                String t = "";
                this.setVisible(false);
                frmScore resultForm = new frmScore(listenServer, game2, t);
                resultForm.setVisible(true);
                break;
            }
            // Thông báo
            case 62: {
                game2 = (Game) msg.getObject();
                String t = "";
                this.setVisible(false);
                frmScore resultForm = new frmScore(listenServer, game2, t);
                resultForm.setVisible(true);
                break;
            }
            // Thông báo người chơi thoát khỏi phòng và out phòng
            case 71: {
                JOptionPane.showMessageDialog(null, "Nguoi choi da thoat khoi phong!", "Message", 1);
                this.setVisible(false);
                frmHome homeForm = new frmHome(listenServer);
                homeForm.setVisible(true);
                break;
            }
            case 100: {
                JOptionPane.showMessageDialog(null, "Đối thủ chưa hoàn thành trận đấu!", "Message", 1);
                break;
            }
        }
    }
