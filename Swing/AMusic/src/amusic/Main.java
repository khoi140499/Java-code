    private void btnNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNextMouseClicked
        if(txtMode.getText().equalsIgnoreCase("auto")==true){
            player.skipForward();
        }
        else if(txtMode.getText().equalsIgnoreCase("manual")==true){
            if(txtPlay.getText().equalsIgnoreCase("2")==true){
                player.stop();
            }
        
            ArrayList<String> st=new ArrayList<String>();
            for(BaiHat as:bh){
                st.add(as.getTenBai());
            }
        
            for(int i=0;i<st.size();i++){
                if(st.get(i).equals(txtHien.getText()+".mp3")==true){
                    String s=st.get(i+1)+"";
                    String[] arr=s.split(".mp3");
                    txtHien.setText(arr[0]);
                    list.setSelectedIndex(i+1);
                    break;
                }
                else if(st.get(st.size()-1).equals(txtHien.getText()+".mp3")==true){
                    String s=st.get(i)+"";
                    String[] arr=s.split(".mp3");
                    txtHien.setText(arr[0]);
                    list.setSelectedIndex(i);
                }
            }
        
            for(BaiHat as:bh){
                if(as.getTenBai().equalsIgnoreCase(txtHien.getText()+".mp3")){
                    player=new MP3Player();
                    File f=new File(as.getDuongDan());
                    player.addToPlayList(f);
                    player.play();
                    btnPlay.setIcon(new ImageIcon(getClass().getResource("/amusic/img/pause_button_30px.png")));
                    txtPlay.setText("2");
                    btnPlay.setToolTipText("Pause");
                }
            }
        }
    }//GEN-LAST:event_btnNextMouseClicked

