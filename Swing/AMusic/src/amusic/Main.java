    private void hienDS(){
        try {
            File f=new File("BaiHat.txt");
            Scanner sc=new Scanner(f);
            while (sc.hasNext()) {
                BaiHat a=new BaiHat(sc.nextLine(), sc.nextLine());
                bh.add(a);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        for(BaiHat as:bh){
            String s=as.getTenBai();
            String[] arr=s.split(".mp3");
            dl.addElement(arr[0]);
            list.setModel(dl);
        }
    }
