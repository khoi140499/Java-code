    private void hiendl(){
        String s1="";
        String s2="";
        try {
            File f=new File("tendangnhap.txt");
            Scanner sc=new Scanner(f);
            while (sc.hasNext()) {
                s1=sc.nextLine();
                s2=sc.nextLine();
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        if(s2.equalsIgnoreCase("true") == true){
            txtUserName.setText(s1);
            check.setSelected(true);
        }
        else if(s2.equalsIgnoreCase("false") == false){
            txtUserName.setText("");
            check.setSelected(false);
        }
    }
