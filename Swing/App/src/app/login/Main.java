    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtUserName.getText().isEmpty()){
            try {
                throw new StringIndexOutOfBoundsException("Không được để trống");
            } catch (StringIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        else{
            try {
                String con="jdbc:mysql://localhost/nnhac";
                String username="root";
                String pass="";
                conn=(Connection) DriverManager.getConnection(con, username, pass);
                String s="select user from login where user=?";
                PreparedStatement ps=conn.prepareStatement(s);
                ps.setString(1, txtUserName.getText());
                if(ps.executeQuery().next()){
                    try {
                        File f1=new File("ten.txt");
                        PrintWriter pr=new PrintWriter(f1);
                        pr.println(txtUserName.getText());
                        pr.close();
                    } catch (Exception e) {
                    }
                    if(check.isSelected()){
                        File f=new File("tendangnhap.txt");
                        PrintWriter pr=new PrintWriter(f);
                        pr.println(txtUserName.getText());
                        pr.println("true");
                        pr.close();
                    }
                    else{
                        File f=new File("tendangnhap.txt");
                        PrintWriter pr=new PrintWriter(f);
                        pr.println(txtUserName.getText());
                        pr.println("false");
                        pr.close();
                    }
                    MApp a=new MApp();
                    setVisible(false);
                    a.setVisible(true);
                }
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

