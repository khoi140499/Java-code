    private boolean kieamTra(String ten){
        for(User as:us){
            if(as.getUser().equalsIgnoreCase(ten)==true){
                return true;
            }
        }
        return false;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtUserName.getText().isEmpty() || txtUserName.getText().length()>15){
            try {
                throw new StringIndexOutOfBoundsException("Khong duoc de trong hoac qua 15 ki tu");
            } catch (StringIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        else if(kieamTra(txtUserName.getText())==true){
            JOptionPane.showMessageDialog(null, "Đã tồn tại user, mời bạn chọn user khác !!!!");
        }
        else{
            try {
                String url="jdbc:mysql://localhost/nnhac";
                String user="root";
                String pass="";
                conn=(Connection) DriverManager.getConnection(url, user, pass);
                String s="insert into login values(?,?)";
                PreparedStatement ps=conn.prepareStatement(s);
                ps.setInt(1, 1+us.size());
                ps.setString(2, txtUserName.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Thành công");
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

