    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        dm3.setRowCount(0);
        Collections.sort(kh, new Comparator<KhachHang>() {
            @Override
            public int compare(KhachHang o1, KhachHang o2) {
                String[] m=o1.getTenKh().split(" ");
                String[] n=o2.getTenKh().split(" ");
                return m[m.length-1].compareToIgnoreCase(n[n.length-1]);
            }
        });
        for(KhachHang as:kh){
            for(Pair a:ds){
                if(a.getKey() == as){
                    Vector<Object> v=new Vector<Object>();
                    v.add(1+tb3.getRowCount());
                    v.add(as.getTenKh());
                    v.add(as.getLoaiKH());
                    v.add(as.getSDT());
                    v.add(as.getDichvu().getTenDV());
                    v.add(as.getDichvu().getGiaCuoc());
                    v.add(txtTime.getText());
                    a6.add(v);
                }
            }
        }tb3.updateUI();
    }//GEN-LAST:event_jButton4ActionPerformed

