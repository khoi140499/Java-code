    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        txtMaKH.setText(10000+tb1.getRowCount()+"");
        if(txtTenKH.getText().length()==0){
            try {
                throw new Exception("Ten khong duoc de trong");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        if(txtDiaChi.getText().length()==0){
            try {
                throw new Exception("Dia chi khong de trong");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        if(txtSDT.getText().charAt(0)!='0' && txtSDT.getText().length()!=9 &&txtSDT.getText().length()!=10){
            try {
                throw new Exception("So dien thoai khong dung ding dang");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        else{
            Vector v=new Vector();
            v.add(txtMaKH.getText());
            v.add(txtTenKH.getText());
            v.add(txtDiaChi.getText());
            v.add(txtSDT.getText());
            v.add(comboLOAI.getSelectedItem().toString());
            a2.add(v);
            tb1.updateUI();
            KhachHang k=new KhachHang(Integer.parseInt(txtMaKH.getText()), txtTenKH.getText(), txtDiaChi.getText(), txtSDT.getText(), comboLOAI.getSelectedItem().toString());
            kh.add(k);
            FILE.writeFile(kh, "KhachHang.txt");
        }
        loadTenKH();
    }//GEN-LAST:event_jButton1ActionPerformed

