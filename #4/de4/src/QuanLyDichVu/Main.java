    private void hienKH(){
        File f=new File("KhachHang.txt");
        if(f.exists()){
            dm1.setRowCount(0);
            FILE.readFile(kh, "KhachHang.txt");
            for(KhachHang as:kh){
                Vector v=new Vector();
                v.add(as.getMaKH());
                v.add(as.getTenKh());
                v.add(as.getDiaChi());
                v.add(as.getSDT());
                v.add(as.getLoaiKH());
                a2.add(v);
            }tb1.updateUI();
        }
    }
    private void hienDV(){
        File f=new File("DichVu.txt");
        if(f.exists()){
            dm2.setRowCount(0);
            FILE.readFile(dv, "DichVu.txt");
            for(DichVu as:dv){
                Vector v=new Vector();
                v.add(as.getMaDV());
                v.add(as.getTenDV());
                v.add(as.getGiaCuoc());
                v.add(as.getDonViTinh());
                a4.add(v);
            }tb2.updateUI();
        }
    }
    private void loadTenKH(){
        comboTenKh.removeAllItems();
        for(int i=0;i<a2.size();i++){
            Vector v=(Vector)a2.get(i);
            comboTenKh.addItem(v.get(1).toString());
        }
    }
    private void loadTenDV(){
        comboTenDV.removeAllItems();
        for(int i=0;i<a4.size();i++){
            Vector v=(Vector)a4.get(i);
            comboTenDV.addItem(v.get(1).toString());
        }
    }
